var numbers = [1,2,3]

// Push - Will add the items at the end of an array
numbers.push(4)
numbers.push(5)
numbers.push(6)

// Unshift - Will add the items at the bengining of an array
numbers.unshift(0)
numbers.unshift(-1)
numbers.unshift(-2)

// Pop - Will delete the items at the end of an array 

numbers.pop()
numbers.pop()
numbers.pop()

// Shift - Will delete the items at the bengining of an array

numbers.shift()
numbers.shift()
numbers.shift()

console.log(numbers)